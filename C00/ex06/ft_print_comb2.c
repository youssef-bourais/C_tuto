#include<unistd.h>

void ft_print_comb2(void)
{
	int	i;
	int	j;

	i = '0';
	while(i <= '9')
	{
		j = '0';
		while(j <= '9')
		{
			write(1, &i, 1);
			write(1, &j, 1);
			write(1, ", ", 2);
			j ++;	
		}
		i ++;
	}
}

